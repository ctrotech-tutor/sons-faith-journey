// Recently used emojis storage functions
export const getRecentEmojis = (): string[] => {
  try {
    const recent = localStorage.getItem('recentEmojis');
    return recent ? JSON.parse(recent) : [];
  } catch {
    return [];
  }
};

export const addRecentEmoji = (emoji: string): void => {
  try {
    const recent = getRecentEmojis();
    const filtered = recent.filter(e => e !== emoji);
    const updated = [emoji, ...filtered].slice(0, 30); // Keep last 30
    localStorage.setItem('recentEmojis', JSON.stringify(updated));
  } catch {
    // Silently fail if localStorage is not available
  }
};

export const clearRecentEmojis = (): void => {
  try {
    localStorage.removeItem('recentEmojis');
  } catch {
    // Silently fail if localStorage is not available
  }
};